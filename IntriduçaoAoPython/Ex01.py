#Exercicio para somar tres valores e obter o quadrado da soma!
print ("Digite numeros inteiros caso solicitado!")

n1 = int(input("Escreva o primeiro numero: "))
n2 = int(input("Digite outro numero: "))
n3 = int(input("Digite o terceiro numero: "))

soma = n1 + n2 + n3
print ("A soma dos tres numeros eh: ", soma )
quadrado = soma * soma 
print ("O quadrado da soma obtida eh: ", quadrado)




